package modelo;

public class Investigador {

	// Atributos
	private String nombre;

	// Constructor
	public Investigador(String nombre) {
		this.nombre = nombre;
	}

	// Getters and Setters
	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

}
